package com.riat;

import com.sun.istack.internal.NotNull;

public class Magazine implements Comparable<Magazine> {
    private String name;
    private int number;
    private int year;


    Magazine() {
        this.name = "Untitled";
        this.number = 1;
        this.year = 2015;
    }

    Magazine(String name, int number, int year) {
        this.name = name;
        this.number = number;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public void print() {
        System.out.println("Magazine: name = " + this.name + "\n\tNumber = " + this.number + "\n\tYear = " + this.year);
    }

    @NotNull
    public int compareTo(Magazine to) {
        int year = to.getYear();
        if (this.year < year) {
            return -1;
        }
        if (this.year == year) {
            return 0;
        }
        return 1;
    }

    public String sprint() {
        return String.valueOf("Magazine: name: = " + this.name + " Number = " + this.number + " Year = " + this.year + "\n");
    }
}
